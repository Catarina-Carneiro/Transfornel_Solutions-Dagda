    private void btnLogarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLogarActionPerformed
        // TODO add your handling code here:
        BasicDataSource dataSource = new BasicDataSource();
        dataSource.setDriverClassName("org.h2.Driver");
        dataSource.setUrl("jdbc:h2:file:./bancoDagada");

        dataSource.setUsername("sa");
        dataSource.setPassword("");

        jdbcTemplate = new JdbcTemplate(dataSource);
        String email = tfEmail.getText();
        String senha = tfSenha.getText();
        List<Usuario> Logar = jdbcTemplate.query(
                "select * from USUARIO where email = ? and senha = ?",
                new BeanPropertyRowMapper(Usuario.class), email, senha);
        if (Logar.isEmpty()) {
            lbResul.setText("Usuario n√£o encontrado");
        } else {
            DashBoard dashBoard = new DashBoard(Logar);
            // abre a tela Dados
            dashBoard.setVisible(true);
            // fecha a tela que esta aberta
            dispose();
        }
    }//GEN-LAST:event_btnLogarActionPerformed

